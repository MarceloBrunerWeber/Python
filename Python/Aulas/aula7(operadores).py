subtracao = 10 - 5
print("Subtração", subtracao)

multiplicacao = 10 * 10
print("Multiplicação", multiplicacao)

divisao= 10 / 2.2 # sempre será float
print("Divisão", divisao)

divisao_inteira = 10 // 2.2 # ponto flutuante cortando as casas decimais 
