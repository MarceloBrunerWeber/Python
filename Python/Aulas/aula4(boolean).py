# Tipo de dado bool (boolean)
"""
Ao questionar algo em um programa só existem duas respostas possíveis: 
sim (True) ou não (False).
Existem vários operadores para "questionar".
Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro por exemplo:
"""
print(3 == 3) # Sim -> True  (Verdadeiro)
print(3 == 9)   # Não -> False (falso)
print(type(True))
print(type(False))
print(type(3 == 3))
print(type(3 == 9))